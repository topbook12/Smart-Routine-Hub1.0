// Smart Routine Hub — Shared types

export type Role = "admin" | "teacher";

export type Program = "bsc" | "msc";

export type DayOfWeek =
  | "Saturday"
  | "Sunday"
  | "Monday"
  | "Tuesday"
  | "Wednesday"
  | "Thursday"
  | "Friday";

