export type ClassType = "theory" | "lab";

export type ChangeType = "cancelled" | "rescheduled" | "room_changed" | "extra_class";

export type NoticeCategory =
  | "academic"
  | "exam"
  | "event"
  | "general"
  | "schedule_change";

export interface User {
  id: string;
  email: string;
  fullName: string;
  designation?: string | null;
  department?: string | null;
  phone?: string | null;
  photoURL?: string | null;
  officeRoom?: string | null;
  bio?: string | null;
  role: Role;
  isActive: boolean;
}

export interface Course {
  id: string;
  name: string;
  code: string;
  creditHours: number;
  type: ClassType;
  semester: number;
  program: Program;
  isActive: boolean;
}

export interface Room {
  id: string;
  roomNumber: string;
  building?: string | null;
  type: "classroom" | "lab" | "seminar";
  capacity: number;
  isActive: boolean;
}

export interface TimeSlot {
  id: string;
  label: string;
  startTime: string;
  endTime: string;
  slotOrder: number;
  isBreak: boolean;
  isActive: boolean;
}

export interface Schedule {
  id: string;
  courseId: string;
  teacherId: string;
  roomId: string;
  timeSlotId?: string | null;
  courseName: string;
  courseCode: string;
  teacherName: string;
  teacherInitials?: string | null;
  roomNumber: string;
  startTime: string;
  endTime: string;
  dayOfWeek: DayOfWeek;
  semester: number;
  program: Program;
  section?: string | null;
  classType: ClassType;
  isActive: boolean;
}

export interface ScheduleChange {
  id: string;
  scheduleId?: string | null;
  changeType: ChangeType;
  originalDay?: string | null;
  originalStartTime?: string | null;
  originalEndTime?: string | null;
  originalRoomId?: string | null;
  originalRoomNumber?: string | null;
  newDay?: string | null;
  newStartTime?: string | null;
  newEndTime?: string | null;
  newRoomId?: string | null;
  newRoomNumber?: string | null;
  effectiveDate?: string | null;
  reason?: string | null;
  courseName?: string | null;
  courseCode?: string | null;
  teacherId?: string | null;
  teacherName?: string | null;
  semester?: number | null;
  program?: string | null;
  changedById?: string | null;
  changedByName?: string | null;
  isActive: boolean;
  createdAt: string;
}

export interface Notice {
  id: string;
  title: string;
  content: string;
  category: NoticeCategory;
  changeType?: string | null;
  scheduleChangeId?: string | null;
  affectedSemester?: number | null;
  affectedProgram?: string | null;
  postedById?: string | null;
  postedByName?: string | null;
  isPinned: boolean;
  isApproved: boolean;
  isAutoGenerated: boolean;
  attachmentURL?: string | null;
  expiryDate?: string | null;
  createdAt: string;
  updatedAt: string;
}

export interface LibraryLink {
  id: string;
  degree: "bsc" | "msc" | "others";
  semester?: number | null;
  url: string;
  title: string;
  isActive: boolean;
}

